const int MM    = 8;
const int NN    = 255;
const int PAD   = 0;
const int FCR   = 0;
const int PRIM  = 1;
const int IPRIM = 1;

#define ALPHA_TO (m_alphaTo) 
#define INDEX_OF (m_indexOf)
#define GENPOLY (m_genpoly)
#define NROOTS (m_nroots)
#define A0 (NN)

inline int MODNN(int x)
{
  while (x >= NN) {
    x -= NN;
    x = (x >> MM) + (x & NN);
  }

  return x;
}
  
